// مطابقة المسميات من المذكر إلى المؤنث — يُفعَّل ديناميكياً حسب المسمى الوظيفي
// للمشترك (me.gender === "female" أو المسمى ينتهي بـ «ة»). يستبدل الكلمات/العبارات
// المذكرة بنظيرتها المؤنثة داخل أي نص عبر مرور واحد (idempotent).
const FEMININE_PAIRS = [
  // عبارات طويلة أولاً (يُعالَج بالترتيب التنازلي للطول)
  ["وكيل شؤون المعلمين", "وكيلة شؤون المعلمات"],
  ["وكيل شؤون الطلاب", "وكيلة شؤون الطالبات"],
  ["الموجه الطلابي", "الموجهة الطلابية"],
  ["معلمو المدرسة", "معلمات المدرسة"],
  ["اسم مدير المدرسة", "اسم مديرة المدرسة"],
  ["نظرة شاملة للطالب", "نظرة شاملة للطالبة"],
  ["سجل أداء الطالب", "سجل أداء الطالبة"],
  ["تصنيف الطلاب", "تصنيف الطالبات"],
  ["تقويم المعلم", "تقويم المعلمة"],
  ["سجل المعلم", "سجل المعلمة"],
  ["لوحة المدير", "لوحة المديرة"],
  // جموع
  ["المعلمون", "المعلمات"],
  ["الطلاب", "الطالبات"],
  // مفرد مبدوء بـ «ال» — يُعالَج بـ lookahead سلبي لتفادي مطابقة الصيغة المؤنثة
  ["المعلم", "المعلمة"],
  ["الطالب", "الطالبة"],
  ["المدير", "المديرة"],
  ["الوكيل", "الوكيلة"],
  ["الموجه", "الموجهة"],
];

// المفرد المذكّر المبدوء بـ «ال» لا يُطابق إذا تبعه لاحقة تأنيث (ة أو ات) —
// هذا يضمن أن إعادة تطبيق الدالة على نص مؤنّث لا يُفسده (idempotent)،
// فلا تتحول «الطالبات» إلى «الطالبةات».
const SINGULAR_WITH_LOOKAHEAD = new Set([
  "المعلم", "الطالب", "المدير", "الوكيل", "الموجه",
]);

const SORTED_PAIRS = [...FEMININE_PAIRS].sort((a, b) => b[0].length - a[0].length);

const PAIR_MAP = new Map(SORTED_PAIRS);
const MATCH_REGEX = new RegExp(
  SORTED_PAIRS.map(([masc]) => {
    const esc = masc.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    return SINGULAR_WITH_LOOKAHEAD.has(masc) ? `${esc}(?!ة|ات)` : esc;
  }).join("|"),
  "g"
);

export function feminineTerm(label) {
  if (!label) return label;
  return label.replace(MATCH_REGEX, (m) => PAIR_MAP.get(m) || m);
}

// يحوّل المسمى حسب جنس المشترك: مؤنّث إذا كان me.gender === "female" أو
// انتهى المسمى الوظيفي (me.title) بـ «ة»، وإلا مذكر.
export function genderTerm(me, label) {
  if (!label) return label;
  const female =
    me?.gender === "female" ||
    (typeof me?.title === "string" && me.title.endsWith("ة"));
  return female ? feminineTerm(label) : label;
}