export function getOnboardingSteps(me) {
  if (!me) return [];
  if (me.role === "admin") return ONBOARDING_STEPS.admin;
  return ONBOARDING_STEPS[me.app_role] || ONBOARDING_STEPS.teacher;
}

export function getRoleWelcomeTitle(me) {
  if (!me) return "مرحباً بك";
  if (me.role === "admin") return "مرحباً بك أيها المدير";
  const title = me.title || "";
  const name = me.display_name || me.full_name || "";
  return title ? `${title} ${name}` : `مرحباً بك ${name}`;
}